function getBasePrefix() {
  return window.location.pathname.startsWith("/www/") ? "/www" : "";
}

function buildPath(path) {
  return `${getBasePrefix()}${path}`;
}

function applyPalette(palette) {
  if (!palette) return;
  document.documentElement.style.setProperty("--accent", palette.accent);
  document.documentElement.style.setProperty("--accent-2", palette.accent2);
}

function getCurrentPageId() {
  const idFromBody = document.body.dataset.page;
  if (idFromBody) return idFromBody;
  const segments = window.location.pathname.split("/").filter(Boolean);
  const lastSegment = segments[segments.length - 1];
  if (pageLookup.has(lastSegment)) return lastSegment;
  const previousSegment = segments[segments.length - 2];
  return pageLookup.has(previousSegment) ? previousSegment : null;
}

